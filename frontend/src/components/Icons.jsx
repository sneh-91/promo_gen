export function ArrowIcon({ direction = "right", className = "" }) {
  const points = direction === "left" ? "12 19 5 12 12 5" : "12 5 19 12 12 19";
  const line = direction === "left" ? { x1: 19, x2: 5 } : { x1: 5, x2: 19 };

  return (
    <svg
      className={className}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth="2"
      strokeLinecap="round"
      strokeLinejoin="round"
      aria-hidden="true"
    >
      <line x1={line.x1} y1="12" x2={line.x2} y2="12" />
      <polyline points={points} />
    </svg>
  );
}

